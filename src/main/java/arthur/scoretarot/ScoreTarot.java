    private void butt_scoreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butt_scoreMouseClicked
        int score;
        int objectif = 0;
        String v_bouts = (String) ccbox_bouts.getSelectedItem();
        int bouts =Integer.parseInt(v_bouts);
        String v_points = txt_points.getText();
        int points = Integer.parseInt(v_points);
        
        switch(bouts){
            case 0:
                objectif = 61;
                break;
            case 1:
                objectif = 51;
                break;
            case 2:
                objectif = 41;
                break;
            case 3:
                objectif = 31;
                break;
        }
        score = points - objectif;
        System.out.println(objectif);
        
        if(butt_petite.isSelected() == true){
            if(score < 0){
                score = score - 25;
            }else{
                score = score + 25;
                if(butt_poignee.isSelected() == true){
                    score = score + 20;
                }
                if(butt_petit.isSelected() == true){
                    score = score + 10;
                }
            }
        }
        if(butt_garde.isSelected() == true){
            if(score < 0){
                score = (score - 25)*2;
            }else{
                score = (score + 25)*2;
                if(butt_poignee.isSelected() == true){
                    score = score + 20;
                }
                if(butt_petit.isSelected() == true){
                    score = score + 10;
                }
            }
        }
        
        String s = Integer.toString(score);
        lbl_score.setText(s);
        
    }//GEN-LAST:event_butt_scoreMouseClicked

