    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String player = JOptionPane.showInputDialog(null,"Entrez le nom du joueur: " ,"Add", JOptionPane.INFORMATION_MESSAGE);
        DefaultTableModel modele = (DefaultTableModel) jtab_players.getModel();
        int j=0;
        for(int i=0;i<jtab_players.getRowCount();i++){
            if(jtab_players.getValueAt(i, 0).equals("")){
                j++;
            }
        }
        if(j==0){
            JOptionPane.showMessageDialog(null,"5 joueurs max !" ,"Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            j = 5-j;
            jtab_players.setValueAt(player, j, 0);
        }
        
        
    }//GEN-LAST:event_jButton2MouseClicked

    private void butt_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butt_addActionPerformed
        DefaultTableModel modele = (DefaultTableModel) jtab_players.getModel();
        for(int i=0;i<=modele.getRowCount()-1;i++){
            for(int j=0;j<=modele.getColumnCount()-1;j++){
                modele.setValueAt("", i, j);
            }
        }
        lbl_num_partie.setText("1");
        
        String[] parties = {"3", "5", "10"};
        JOptionPane jop = new JOptionPane();
        int choix = jop.showOptionDialog(null, "Combien de parties souhaitez-vous jouer ?","Nombre de parties",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE,null,parties,parties[0]);
        int nb_parties;
        if("3".equals(parties[choix])){
            nb_parties = 3;
        }
        else{
            if("5".equals(parties[choix])){
                nb_parties = 5;
            }
            else{
                nb_parties = 10;
            }
        }
        lbl_nb_parties.setText(parties[choix]);
        for(int i=1;i<=nb_parties;i++){
            String j = String.valueOf(i);
            jtab_players.getColumnModel().getColumn(i).setHeaderValue(j);
        }
        jtab_players.getColumnModel().getColumn(nb_parties+1).setHeaderValue("Total");
    }//GEN-LAST:event_butt_addActionPerformed

